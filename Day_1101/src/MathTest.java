
public class MathTest {

	public static void main(String[] args) {
		
		// static double	ceil(double a)
		// ceil()메소드는 올림
		System.out.println(Math.ceil(56.2390));
		
		// static double	floor(double a)
		// floor()메소드는 버림
		System.out.println(Math.floor(45.5454));
		
		// static int	max(int a, int b)
		// int max()는 둘중 큰값나오게(최대값)
		System.out.println(Math.max(12, 23));
		
		// static double	pow(double a, double b)
		// a에서 b를 거듭제곱해주는거
		System.out.println(Math.pow(2.00, 4.00));
		
		// static long	round(double a)
		// 반올림하는 메소드
		System.out.println(Math.round(23.723232423424));
		
		// static double	sqrt(double a)
		// 루트 씌운다
		System.out.println(Math.sqrt(36.00));
		

	}

}
