
public class Second {
	public static void main(String[] args) {
		
		System.out.println(2022); //int형 데이터 타입 정수뒤에L이나l이 붙어있으면 long형
		System.out.println(42.195); //double 42.195F(Float형으로 만들기위해 뒤에 f붙임)
		System.out.println("서울시 강남구");
		System.out.println('@'); //''문자형 : 안에는 한글자만 들어가야함 
		System.out.println(true); //boolean 리터럴
		System.out.println(false); //boolean 리터럴
		
		//  // -> 한줄주석 /**/ -> 여러줄주석
		
		//기초 자료형(Data Type) == Primitive 자료형 8종류가 있음
		
		/* 1) 정수형 : byte(1), short(2), int(4)기본 정수는 무조건, long(8)
		 * 2) 실수형 : float(4), double(8) double이 기본
		 * 3) 문자형 : char(2) 안에는 한글자만 들어가야함
		 * 4) 불린형 : boolean(1) 참이나 거짓 나타내는거
		 * 
		 *  byte가 담을수 있는 값의 범위 : -2^7 ~ 2^7-1 = -128 ~ 127*/
	}
	

}
