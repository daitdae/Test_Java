
public class TowDimTest_01 {

	public static void main(String[] args) {
		
		int[][] ary = new int[3][5];
		
		System.out.println("ary.length: " +ary.length);
		System.out.println("ary[0].length: " +ary[0].length);
		
		System.out.println();
		
		System.out.println("ary: " +ary);
		System.out.println("ary[0]: " +ary[0]);
		System.out.println("ary[0][0]: " +ary[0][0]);

	}

}
