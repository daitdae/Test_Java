
public class OperatorTest_01 {

	public static void main(String[] args) {
		
		int result;
		result = 10 + 20;
		System.out.println(result);
		
		result = 10-20; //변수 선언은 한번만
		System.out.println(result);
		
		result = 10*20;
		System.out.println(result);
		
		result = 10/20;  //나누기는 정수 정수로 나누면 결과 정수로 나온다
		System.out.println(result);
		
		result = 10 % 20; //나머지, 몫이 없으니 다 나머지로 나온다
		System.out.println(result);
		
	}

}
